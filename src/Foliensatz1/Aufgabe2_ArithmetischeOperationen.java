package Foliensatz1;

public class Aufgabe2_ArithmetischeOperationen {

    public static void main(String[] args) {
        int a = 4;
        double b = 5;

        double sum = a+b;
        double subtraction = a-b;
        double multiplication = a*b;
        double division = a/b;

        System.out.println(sum + ", " + subtraction + ", " + multiplication + ", " + division);
    }
}
